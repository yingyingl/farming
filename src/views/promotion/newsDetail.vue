DB13/T 2366 冬小麦播后镇压技术规程<br/><br/>
3  术语和定义<br/><br/>
3.1  下列术语和定义适用于本文件。<br/><br/>
黄骅旱碱麦
种植在土壤含盐量为1‰～3‰的河北省滨海平原盐碱干旱生态类型区(以黄骅市及周边地区为 代表)，具有沧州小红芒品种特征的一类中强筋冬小麦。<br/><br/>
4  播前准备<br/><br/>
4.1    施足底肥<br/><br/>
结合旋耕， 亩施腐熟有机肥1500 kg～2000 kg、低氯型复合肥40 kg（N-P2 O5-K2O=20-18-7），有 机肥可每2年施一次。肥料的使用应符合NY/T 496的规定。<br/><br/>
4.2   精细整地<br/><br/>
采用深翻+旋耕的整地方式，每3年深翻一次，深度不小于30 cm。播前结合施底肥旋耕，耕深不 小于15 cm，作业质量应符合NY/T 499中的规定。<br/><br/>
5  品种选择及种子处理<br/><br/>
5.1    品种选择<br/><br/>
选择国家或河北省品种审定委员会审定的适宜本区域种植的具有沧州小红芒主要特征（分蘖力 强， 株高较高， 穗纺锤形、抗旱性强）的一类中强筋小麦品种，如：捷麦19、沧麦6005等。种子质量 应符合GB 4404.1规定。<br/><br/>
5.2   种子处理<br/><br/>

针对当地主要地下害虫及土传病害，选用高效低毒的农药包衣或拌种，种子包衣质量应符合GB/T 15671规定。<br/><br/>
6  播种<br/><br/>
6.1    播期<br/><br/>
最佳播期为9月30日至10月7日。<br/><br/>
6.2   播量<br/><br/>
最佳播期内，亩播量12.5 kg，每推迟1 d，播量增加0.5 kg，最大亩播量不超过22.5 kg。<br/><br/>
6.3   播种要求<br/><br/>
适播期内耕层（0 cm～20 cm）土壤相对含水量65%～75%时即可播种。条播，行距15 cm～20 cm， 播深3 cm～5 cm，深浅一致。<br/><br/>
6.4   播后镇压<br/><br/>
播后按照DB13/T 2366中的规定机械镇压， 镇压强度200 kg/m。<br/><br/>
7  冬前管理<br/><br/>
7.1    冬前化学除草<br/><br/>
按照NY/T 2914-2016中5.3.1规定执行，农药的使用应符合GB/T 8321中的规定。<br/><br/>
7.2   冬前镇压<br/><br/>
麦田在上冻前进行冬前镇压，镇压强度200 kg/m。<br/><br/>
8  春季管理<br/><br/>
8.1    锄划镇压<br/><br/>
返青前进行麦田锄划和镇压。<br/><br/>
8.2   追肥<br/><br/>
8.2.1  趁墒追肥<br/><br/>
返青前顶凌追肥，每亩施20 kg尿素。<br/><br/>
8.2.2  肥料水溶追施<br/><br/>
春季干旱无法趁墒追肥时，每亩将20 kg尿素和500 g磷酸二氢钾溶解于1 m3～2 m3水中，利用追肥 机追施。<br/><br/>
8.3   病虫害防治<br/><br/>
应主要防治锈病、赤霉病、白粉病、蚜虫、吸浆虫、红蜘蛛和麦叶蜂等，重点防治红蜘蛛和蚜 虫，防治方法按照NY/T 2914-2016中5.3.1和5.3.7规定执行。<br/><br/>
9  中后期管理<br/><br/>
9.1    喷施叶面肥<br/><br/>
小麦拔节至孕穗期叶面喷施0.2%～0.3%的磷酸二氢钾和1%～2%的尿素水溶液补施肥料。<br/><br/>
9.2   一喷三防<br/><br/>

根据病虫害发生情况及气候条件，选用适宜的杀虫剂、杀菌剂和植物生长调节剂，在小麦抽穗 前和开花后，应叶面喷施各1次。药剂应现配现用，小麦收获前20 d内不应用药。<br/><br/>
10  收获<br/><br/>
于蜡熟末期至完熟期及时收获。<br/><br/>
