黄骅旱碱麦生产技术规程<br/><br/>

1  范围<br/><br/>
本文件规定了黄骅旱碱麦生产技术的术语和定义、播前准备、品种选择及种子处理、播种、冬 前管理、春季管理、中后期管理及收获等。
本文件适用于土壤含盐量为1‰～3‰的河北省滨海平原盐碱干旱生态类型区的冬小麦生产。<br/><br/>
2  规范性引用文件<br/><br/>
下列文件中的内容通过文中的规范性引用而构成本文件必不可少的条款。其中，注日期的引用 文件， 仅该日期对应的版本适用于本文件；不注日期的引用文件，其最新版本（包括所有的修改单） 适用于本文件。<br/>
